use Mix.Config

## Available parameters

# Set the app in which to look for config files
#config :sweetconfig, app: <name_of_your_application>

# Specify the directory to load configs from
#config :sweetconfig, dir: <path>

# Specify which config keys for which apps should be written to application env
#config :sweetconfig, write_to_env: [app1: [:a, :b], app2: [:c]]
